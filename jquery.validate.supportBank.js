jQuery.validator.addMethod("support_bank", function(value, element, params) {
    value = value.replace(/\s+/g, '');
    return this.optional(element) || support_bank(value, element, params);
}, "您输入的银行卡号不支持开通存管。");
function support_bank(value, element, params) {
    var options = $.extend({
      cardTypes: [
        '借记卡(储蓄卡)',
        '借贷卡(信用卡)',
        '准借贷卡(信用卡)',
        '预付款卡(信用卡)'
      ],
      errorLength: '您输入的银行卡号(位数)不正确。',
      errorType: '不支持{0}。',
      msgIncomplete: '此卡可以进行绑定，但不能用于快捷充值。'
    }, params.options);
    $(element).attr('data-tips', '');
    if(params.notSupport && params.notSupport.prefix) {
        var prefixReg = new RegExp('^' + params.notSupport.prefix.join('|'));
        if(prefixReg.test(value)) {
            return false;
        }
    }
    var check = 0;
    var errMsg = '';
    for(var i=2;i<11;i++) {
        var key = value.substr(0, i);
        if(!allBanks[key]) {
            continue;
        }
        check++;
        if(value.length != allBanks[key][1]) {//长度不匹配
            errMsg = options.errorLength;
            continue;
        }
        if(params.type != allBanks[key][2]) {//类型不匹配
            errMsg = options.errorType.replace('{0}', options.cardTypes[Number(allBanks[key][2]) - 1]);
            continue;
        }
        //匹配不支持的银行
        if(params.notSupport && params.notSupport.name) {
            var nameReg = new RegExp(params.notSupport.name.join('|'));
            if(nameReg.test(allBanks[key][0])) {
                return false;
            }
        }
        //匹配支持的银行
        if(params.support) {
            for(var j=0;j<params.support.length;j++) {
                var nameReg = new RegExp(params.support[j].name || params.support[j]);
                if(nameReg.test(allBanks[key][0])) {
                    if(params.support[j].notSupport) {
                        if(params.support[j].notSupport.length) {
                            var lengthReg = new RegExp(params.support[j].notSupport.length.join('|'));
                            if(lengthReg.test(allBanks[key][1])) {
                                return false;
                            }
                        }
                        if(params.support[j].notSupport.name) {
                            var nameReg = new RegExp(params.notSupport.name.join('|'));
                            if(nameReg.test(allBanks[key][0])) {
                                return false;
                            }
                        }
                    }
                    return true;
                }
            }
        }
    }
    if(check) {
        var checked = !!!errMsg;
        if(!errMsg) {
            errMsg = options.msgIncomplete;
        }
        $(element).attr('data-tips', errMsg);
        return checked;
    }
    return false;
}